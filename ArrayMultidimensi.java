public class ArrayMultidimensi {
    public static void main(String args[]) {
       // program ini berguna untuk menampilkan multidimensi
       String[][] dogs = {{ "terry", "brown" },
                          { "Kristin", "white" },
                          { "toby", "gray"},
                          { "fido", "black"}};
       System.out.print( dogs[0][0] );
    }
}

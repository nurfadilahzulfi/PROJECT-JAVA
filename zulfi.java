
public class zulfi {
    //public class Zulfi (zulfi itu harus sama dengan nama file nya)
    public static void main(String args[]) {
        //ini merupakan fungsi untuk main
        int i = 23;
        int j = 44;
        double x = 24.475;
        double y = 6.89;
        
        System.out.println("Nilai Pada Variabel adalah :");
        System.out.println("    i = " + i);
        System.out.println("    j = " + j);
        System.out.println("    x = " + x);
        System.out.println("    y = " + y);
        System.out.println("");
        // ini program untuk penjumlahan
        System.out.println("Ini program untuk penjumlahan : ");
        System.out.println("i + j = " + (i+j));
        System.out.println("i + x = " + (i+x));
        System.out.println("i + y = " + (i+y));
        System.out.println("y + x = " + (y+x));
        
        System.out.println("");
        //ini program untuk pengurangan
        System.out.println("Ini program untuk pengurangan : ");
        System.out.println("y - x = " + (y-x));
        System.out.println("x - y = " + (x-y));
        System.out.println("i - j = " + (i-j));
        System.out.println("j - x = " + (j-x));
        System.out.println("");
        
        //Ini program untuk perkalian
        System.out.println("Ini program untuk perkalian : ");
        System.out.println("i * x = " + (i*x));
        System.out.println("j * x = " + (j*x));
        System.out.println("j * j = " + (j*j));
        System.out.println("x * x = " + (x*x));
        System.out.println("");
        
        //ini program untuk pembagian
        System.out.println("Ini program untuk pembagian : ");
        System.out.println("j / j = " + (j/j));
        System.out.println("x / x = " + (x/x));
        System.out.println("j / i = " + (j/i));
    }
}
